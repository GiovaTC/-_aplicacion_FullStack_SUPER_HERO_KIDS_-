CREATE TABLE SUPER_HEROES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100),
    PODER VARCHAR2(100),
    NIVEL NUMBER,
    UNIVERSO VARCHAR2(100),
    PRIMARY KEY(ID)
);

CREATE TABLE JUEGOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100),
    CATEGORIA VARCHAR2(100),
    EDAD_MINIMA NUMBER,
    PRIMARY KEY(ID)
);

CREATE TABLE USUARIOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(100),
    EMAIL VARCHAR2(100),
    PUNTAJE NUMBER,
    PRIMARY KEY(ID)
);

COMMIT;